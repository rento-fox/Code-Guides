# Adding two numbers
a = 2
b = 3
print(a + b) # 5

# Extra variable to hold the sum
a = 2
b = 3
print('a is ' + str(a) + ', and b is ' + str(b)) # a is 2, and b is 3
c = a + b
print('The sum a + b is ' + str(c)) # The sum a + b is 5

# Update the value by adding 1
a = 2
a = a + 1
print(a) # 3

# Decrement a value by 3
a = 5
a -= 3
print(a) # 2

