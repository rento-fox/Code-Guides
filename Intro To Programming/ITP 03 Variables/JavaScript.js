// Use a variable in a string
const string = 'Jane';
console.log('My name is ' + string); // My name is Jane

// Add two string variables together
const a = 'John';
const b = 'My name is ';
console.log(b + a); // My name is John

// Adding two numbers
const int1 = 2;
const int2 = 3;
console.log(int1 + int2); // 5

// Extra variable to hold the sum
const num1 = 2;
const num2 = 3;
console.log('num1 is ' + num1 + ', and num2 is ' + num2); // num1 is 2, and num2 is 3
const sum = num1 + num2;
console.log('The sum num1 + num2 is ' + sum); // The sum num1 + num2 is 5

// Update the value by adding 1
let number1 = 2;
number1 = number1 + 1;
console.log(number1); // 3

// Decrement a value by 3
let number2 = 5;
number2 -= 3;
console.log(number2); // 2

