public class Java9_5 {
    public static void main(String[] args) {
        System.out.println("Hello Java.java"); // Hello Java.java

        // Logical operators

        // && (Logical AND)
        System.out.println(10 == 5 && 10 != 5); // false

        // || (Logical OR)
        System.out.println(10 == 5 || 10 != 5); // true

        // ! (Logical NOT)
        Boolean x = true;
        System.out.println(! x); // false
    }
}

