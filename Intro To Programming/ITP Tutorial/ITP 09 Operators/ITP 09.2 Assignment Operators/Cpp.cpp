#include <iostream>
using namespace std;

int main() {
    cout << "Hello Cpp.cpp\n"; // Hello Cpp.cpp

    // Assignment operators

    // = (Assign)
    int x = 10;
    cout << x; // 10
    cout << "\n";

    // += (Add and assign)
    int x2 = 10;
    x2 += 5;
    cout << x2; // 15
    cout << "\n";

    // -= (Subtract and assign)
    int x3 = 10;
    x3 -= 5;
    cout << x3; // 5
    cout << "\n";

    // *= (Multiply and assign)
    int x4 = 10;
    x4 *= 5;
    cout << x4; // 50
    cout << "\n";

    // /= (Divide and assign)
    int x5 = 10;
    x5 /= 5;
    cout << x5; // 2
    cout << "\n";

    return 0;
}

