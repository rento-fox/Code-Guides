# Comparison operators

# == (Equal to)
print(10 == 5) # False

# != (Not equal to)
print(10 != 5) # True

# > (Greater than)
print(10 > 5) # True

# < (Less than)
print(10 < 5) # False

# >= (Greater than or equal to)
print(10 >= 5) # True

# <= (Less than or equal to)
print(10 <= 5) # False

