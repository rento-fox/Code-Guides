print('Hello Python.py') # Hello Python.py

# Check variable data type
number = 3
string = "Hello"
print(type(number)) # <class 'int'>
print(type(string)) # <class 'str'>

# String data type
message = 'Hello, World!'
print(message) # Hello, World!

# Integer data type
x = 7
print(x) # 7

# Float data type
pi = 3.14
print(pi) # 3.14

# Boolean data type
bool = True
print(bool) # True

# Casting data types
a = '18'
b = int(a) + 3
print(b) # 21

# None, or null
variable = None
print(variable) # None
print(type(variable)) # <class 'NoneType'>

