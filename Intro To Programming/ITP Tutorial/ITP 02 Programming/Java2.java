public class Java2 {
    public static void main(String[] args) {
        System.out.println("Hello Java.java"); // Hello Java.java

        // for loop in Java
        for (int i = 5; i > 0; i--) {
            System.out.println(i); // (prints numbers 5 to 1 vertically)
        }
        System.out.println("Liftoff!"); // Liftoff!
    }
}

