# Assignment operators

# = (Assign)
x = 10
print(x) # 10

# += (Add and assign)
x = 10
x += 5
print(x) # 15

# -= (Subtract and assign)
x = 10
x -= 5
print(x) # 5

# *= (Multiply and assign)
x = 10
x *= 5
print(x) # 50

# /= (Divide and assign)
x = 10
x /= 5
print(x) # 2.0

