public class Java9_3 {
    public static void main(String[] args) {
        System.out.println("Hello Java.java"); // Hello Java.java

        // Assignment operators

        // = (Assign)
        int x = 10;
        System.out.println(x); // 10

        // += (Add and assign)
        int x2 = 10;
        x2 += 5;
        System.out.println(x2); // 15

        // -= (Subtract and assign)
        int x3 = 10;
        x3 -= 5;
        System.out.println(x3); // 5

        // *= (Multiply and assign)
        int x4 = 10;
        x4 *= 5;
        System.out.println(x4); // 50

        // /= (Divide and assign)
        int x5 = 10;
        x5 /= 5;
        System.out.println(x5); // 2
    }
}

