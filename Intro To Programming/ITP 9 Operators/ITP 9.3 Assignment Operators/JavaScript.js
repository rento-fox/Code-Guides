// Assignment operators

// = (Assign)
let x = 10
console.log(x) // 10

// += (Add and assign)
let x2 = 10
x2 += 5
console.log(x2) // 15

// -= (Subtract and assign)
let x3 = 10
x3 -= 5
console.log(x3) // 5

// *= (Multiply and assign)
let x4 = 10
x4 *= 5
console.log(x4) // 50

// /= (Divide and assign)
let x5 = 10
x5 /= 5
console.log(x5) // 2

