#include <iostream>
#include <cmath>
using namespace std;

int main() {
    cout << "Hello Cpp.cpp\n"; // Hello Cpp.cpp

    // Arithmetic operators

    // + (Addition)
    cout << 10 + 5; // 15
    cout << "\n";

    // - (Subtraction)
    cout << 10 - 5; // 5
    cout << "\n";

    // * (Multiplication)
    cout << 10 * 5; // 50
    cout << "\n";

    // / (Division)
    cout << 10 / 5; // 2
    cout << "\n";

    // % (Modulus)
    cout << 10 % 5; // 0
    cout << "\n";

    // ** (Exponentiation)
    cout << pow(2, 3);
    cout << "\n"; // 8

    return 0;
}

