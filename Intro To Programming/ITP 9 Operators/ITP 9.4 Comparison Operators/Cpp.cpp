#include <iostream>
using namespace std;

int main() {
    cout << "Hello Cpp.cpp\n"; // Hello Cpp.cpp

    // Comparison operators

    // == (Equal to)
    cout << (10 == 5);
    cout << "\n"; // 0

    // != (Not equal to)
    cout << (10 != 5);
    cout << "\n"; // 1

    // > (Greater than)
    cout << (10 > 5);
    cout << "\n"; // 1

    // < (Less than)
    cout << (10 < 5);
    cout << "\n"; // 0

    // >= (Greater than or equal to)
    cout << (10 >= 5);
    cout << "\n"; // 1

    // <= (Less than or equal to)
    cout << (10 <= 5);
    cout << "\n"; // 0

    return 0;
}

